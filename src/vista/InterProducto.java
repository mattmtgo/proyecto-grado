    private void jButton_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_GuardarActionPerformed

        Ctrl_Producto controlProducto = new Ctrl_Producto();
        Producto producto = new Producto();

        String nombre = txt_nombre.getText().trim();
        String cantidadTxt = txt_cantidad.getText().trim();
        String precioTxt = txt_precio.getText().trim();
        String descripcion = txt_descripcion.getText().trim();
        String iva = jComboBox_iva.getSelectedItem().toString().trim();
        String categoria = jComboBox_categoria.getSelectedItem().toString().trim();

        // üîπ VALIDAR CAMPOS VAC√çOS
        if (nombre.isEmpty() || cantidadTxt.isEmpty() || precioTxt.isEmpty() || descripcion.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, completa todos los campos obligatorios.", "Campos vac√≠os", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // üîπ VALIDAR N√öMEROS
        int cantidad = 0;
        double precio = 0.0;
        try {
            cantidad = Integer.parseInt(cantidadTxt);
            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(null, "La cantidad debe ser un n√∫mero mayor que cero.", "Dato inv√°lido", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "La cantidad debe ser un n√∫mero v√°lido (por ejemplo, 15).", "Formato incorrecto", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Cambiar coma por punto si el usuario lo escribe as√≠
            precioTxt = precioTxt.replace(",", ".");
            precio = Double.parseDouble(precioTxt);
            if (precio <= 0) {
                JOptionPane.showMessageDialog(null, "El precio debe ser un n√∫mero mayor que cero.", "Dato inv√°lido", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El precio debe ser un n√∫mero v√°lido (por ejemplo, 15000 ).", "Formato incorrecto", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // üîπ VALIDAR IVA
        if (iva.equalsIgnoreCase("Seleccione IVA:")) {
            JOptionPane.showMessageDialog(null, "Por favor, selecciona un tipo de IVA.", "Dato faltante", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // üîπ VALIDAR CATEGOR√çA
        if (categoria.equalsIgnoreCase("Seleccione Categor√≠as:") || categoria.equalsIgnoreCase("Seleccione Categor√≠a:")) {
            JOptionPane.showMessageDialog(null, "Por favor, selecciona una categor√≠a.", "Dato faltante", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // üîπ VALIDAR SI EL PRODUCTO YA EXISTE
        if (controlProducto.existeProducto(nombre)) {
            JOptionPane.showMessageDialog(null, "El producto ya existe en la base de datos.", "Duplicado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // üîπ SI TODO EST√Å BIEN, GUARDAR
        try {
            producto.setNombre(nombre);
            producto.setCantidad(cantidad);
            producto.setPrecio(precio);
            producto.setDescripcion(descripcion);

            // Porcentaje IVA
            if (iva.equalsIgnoreCase("No grava IVA")) {
                producto.setPorcentajeIva(0);
            } else if (iva.equalsIgnoreCase("19%")) {
                producto.setPorcentajeIva(19);
            }

            // ID categor√≠a
            this.IdCategoria();
            producto.setIdCategoria(obtenerIdCategoriaCombo);
            producto.setEstado(1);

            if (controlProducto.guardar(producto)) {
                JOptionPane.showMessageDialog(null, "Producto registrado exitosamente.");
                this.CargarComboCategorias();
                this.jComboBox_iva.setSelectedItem("Seleccione IVA:");
                this.Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar el producto. Intente nuevamente.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Ocurri√≥ un error inesperado al guardar el producto.", "Error", JOptionPane.ERROR_MESSAGE);
            System.err.println("Error en guardar producto: " + e);
        }

    }//GEN-LAST:event_jButton_GuardarActionPerformed


